                    <h1>A' level</h1>
                    <p><img src="img/Alevel.jpg" class="img-responsive" alt="about image">
                        The A' Level Program provides a demanding, expansive educational experience to a select group of diverse, talented and intellectually sophisticated students. Through this program we aim to prepare our students for entry to the great universities of the world! Our excellent and experienced A' Level faculty ensure that our students are given the best guidance for success at the A' Level examinations.

                        The total course for the A' Level runs over a period of two academic years. The Class X students who have graduated from Scholastica may apply for admission in the A' Level course. Each student is admitted on a provisional basis, subject to their successful completion of the O' Level to the satisfaction of the school. At this level, students typically take 3 or 4 subjects.

                        We offer several science and commerce based subjects, and students are also encouraged to take co-curricular subjects such as media studies, physical education, music or drama. There are no mandatory subjects at this level of specialization. Subjects offered at the Aâ€™ Level include Economics, Physics, Chemistry, Business, Biology, Accounting, Computer Science, Mathematics, and from the 2016-17 session, we will also offer Law, Psychology and English.

                        The structure of the A' Level program is such that students take one set of exams at the end of Class XI (the Advanced Subsidiary - 'AS') and their final public examinations at the end of Class XII (typically referred to as the 'A2').
                    </p>
