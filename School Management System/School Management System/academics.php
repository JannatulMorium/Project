                    <h1>O' level</h1>
                    <p><img src="img/Olevel.jpg" class="img-responsive" alt="about image">
                        Scholastica's O' Level program encourages students to engage, invent, manage and compete - equipping them for eventual success in the public examinations under Cambridge International Examinations.

                        Class IX is very important because this is when students begin to prepare for the public examination that will take them to the next stage of their academic career. Students typically choose 8 subjects at this level. The syllabus is chalked out in such a way that the bulk of the work is covered in Class IX. We offer several science, humanities and commerce subjects, and students are also encouraged to take co-curricular subjects such as media studies, physical education, music or drama. Maths, English and Bangla remain core subjects that are mandatory for all students. In Class X, students focus on revision work followed by two mock exams which are held in March or April of each year.

                        Subjects offered in the Oâ€™ Level include Additional Maths, History, Geography, Physics, Chemistry, Biology, Business Studies, Environmental Management, Economics, Principles of Accounts, Bangladesh Studies, Computer Science, and Art & Design. From the 2016-17 session, we will also offer Enterprise and Combined Science.

                        The teaching program at Scholastica is a complete one; we cover the O' Level syllabus thoroughly and undertake overall revision effectively. Regular written work is done throughout the course either at home or under supervision in class to reinforce learning and to give the students the maximum amount of practice in taking exams. Of course, feedback on assessment is an integral part of the learning process.

                        The school is very strict about attesting students' admission forms for the public examination. It will only do so for the examinations according to the prescribed schedule; therefore, students who take advance exams or additional subjects may not be able to avail the facility of sitting for the exams in our own premises and the school will not take the responsibility of completing the syllabus or the teaching program for such subjects.
                    </p>
