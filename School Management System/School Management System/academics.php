                    <h1>Academic Service and Resources</h1>
                    <p><img src="img/AcaSup.jpg" class="img-responsive" alt="about image">
                        Curriculum Development Unit

                        Scholastica's Curriculum Development Unit (CDU) is an independent department that oversees the academic program. The unit ensures that a high standard of education is delivered uniformly at all levels of the school and ensures best practices in teaching. It monitors the delivery of the academic program and comes up with effective services and strategies for imparting our curriculum and maintaining consistency. The unit also oversees the planning of the annual syllabus based on the custom-designed curriculum of the school.

                        The unit also works on quality control and the development of curriculum and teaching resources. Developing textbooks and workbooks is a key component of this unit's work. These books are used in Scholastica and in numerous schools throughout the country.

                        CDU plays an important role in faculty supervision and training. The unit researches and develops the academic curriculum and teaching resources to suit modern teaching and learning needs and promotes a progressive and dynamic approach to education.

                        Special Education Program

                        All children should be entitled to a broad, balanced and purposeful curriculum and Scholastica is committed to ensuring all students can achieve at their highest potential. Some children have varying needs that may require them to be given extra attention in order to keep up with the standard set by the school. Scholastica has developed, therefore, a Special Education Program, which is a set of policies and planned activities, based on global best principles and practices, to cater to the special educational needs of students in Scholastica. In many cases, these students are asked to attend specialized classes (called "Skills Labs"), which are small and allow very close teacher-student interaction. In this way, students who are below the standard of the class are given special attention to build their skills and achieve academic success up to their full potential.
                    </p>
