                        The school year is divided into four quarters, i.e. two terms. The First Term begins in August (after the summer vacation) and the Second Term begins in January (after the winter vacation).Winter Break, Spring Break, Summer Break and the two Eid breaks are the mainscheduled vacations during the academic year.



                        The dates of all the school vacations and other important dates are given in the Annual School Calendar, which is included in every student's School Diary. In addition, parents can download the calendar from the downloads section of our website. Click here to access those documents.
