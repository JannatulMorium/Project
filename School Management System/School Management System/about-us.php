                      <h1>Educational Philosopy</h1>
                      <p><img src="img/Ep.jpg" class="img-responsive" alt="about image">
                      In Scholastica, we implement modern, international best practices in education. We believe in teaching that...

is student-focused,
is thought-provoking and creative
takes into account the needs of individuals and
prepares students for the real world.
We teach our students to be...

independent learners:  we don't tell them everything, we encourage them to learn for themselves
curious:  we encourage them to ask questions and challenge us and each other
critical thinkers:  we ensure students understand what they are learning, rather than just memorizing what they read or hear
willing to take challenges:  we push students to think at higher levels and apply their skills, rather than being intimidated by new types of questions or tasks
creative:  : we encourage students to apply imagination and think outside-the-box, and we reward students who go beyond the book or what they have been taught.
All subjects are taught using interactive and activity-based methods. Asking questions is always encouraged and students are given opportunities to share their own opinion and think critically about the issues at hand. Important skills are also developed through the academic program, so that students are able to keep on learning even after they graduate.




