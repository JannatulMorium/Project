                      <h1>History</h1>
                      <p><img src="img/History.jpg" class="img-responsive" alt="about image">
                      In the sun-scorched summer of 1977, an ambitious and determined woman negotiated to rent a two-storied building for a school, just off the main road near Dhanmondi Lake. Mrs. Yasmeen Murshed wanted to call the school "Scholastica", a purposeful response to the need for an organized school-leaving course in Bangladesh using English as the medium of instruction.

Mrs. Murshed began her career in education as a teacher in various institutions and after the Liberation war she began teaching small groups of students at home, which led to the establishment of 'Scholastica Tutorial' in 1977. The school slowly grew and developed under her leadership and now, called 'Scholastica', it is situated in several locations in the city, including Dhanmondi, Mirpur, Gulshan and Uttara.

The school is currently managed by her daughter, Syeda Madiha Murshed, and Mrs. Murshed remains on the board of management. Madiha is the Managing Director and is responsible for overall operations and development of the school.

Over the years, the school has changed and developed significantly, and yet it has retained its essential characteristics-striving for the highest standards in education, while emphasizing whole child development through sports, events, the arts and all kinds of other extra-curricular. Scholastica continues to be one of the largest and most highly-regarded private schools in Bangladesh.

. </p>
