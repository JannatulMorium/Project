                      <h1>Our Campuses</h1>
                      <p><img src="img/O.jpg" class="img-responsive" alt="about image">
                      We are proud of our two custom-built campuses in Uttara and Mirpur. These facilities allow us to provide safe and comfortable environments for learning, as well as state-of-the-art infrastructure that rivals schools at a global level.

Each of the custom-built campuses has been carefully designed and provides numerous facilities for our faculty and students, including:

Spacious and comfortable, air-conditioned classrooms
High school classrooms which are equipped with smartboards and projectors
An indoor multipurpose hall, used for events, performances and sports
Outdoor hard-courts that host sports tournaments throughout the year
Fully equipped labs for biology, chemistry, physics and computer studies
Library and teacher's resource center
Cafeteria and fully-operational canteen services
Fully stocked bookshop and fee collection booth
Our campus in Mirpur, more recently built, also houses a swimming pool.

Together with these two custom-built campuses we have four more campuses in Dhanmondi, Gulshan and Uttara, which are rented. In order to ensure safe and comfortable environments for learning we have renovated these rented premises and customized them to maximize the school's facilities.

All our classrooms are air-conditioned, and we have designed them to provide comfortable, safe and positive learning environments for our students.



