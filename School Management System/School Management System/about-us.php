Scholastica continues to be a pioneer in education in Bangladesh. The management of the school pursues development and improvement relentlessly.

What makes Scholastica one of the premier schools in the country?   We believe it is:

our emphasis on training for faculty and management
our attention to whole child development through extra-curricular activities
our respect for each and every student who is treated as an individual
our aspirations to implement international best practices in teaching
our investment in technology to enhance the quality of our services
our development of custom-built campuses that boast excellent facilities for sports, visual and performing arts and leadership opportunities
These are the principles on which we continue to improve and develop our academic program, facilities and services. And these are the principles that underlie our success and excellent reputation today.

