                      <h1>ALumni Association</h1>
                      <p><img src="img/Al.jpg" class="img-responsive" alt="about image">
                   The Scholastica Alumni Association (SAA) is an organization of former Scholasticans formed to build and retain the special bond between alums and this institution. In July 2004, with the help of three Founder Members, the Scholastica Alumni Association was launched in a grand ceremony at STM Hall, Uttara.

The Association is committed to bringing together all former Scholasticans who have conquered different corners of the globe. The association is also involved in charity work for the community at large and has raised funds for various welfare projects in the country.

The Alumni Association conducts sporting, cultural and social events throughout the year, and alums are regularly invited to events on campus to maintain close connections with the school.

Any former student of Scholastica who is 18 years of age and has completed at least one year in the institution can become a member of the Alumni Association through online registration here.

We would like to keep in touch with all Scholasticans to share our news and updates and also to hear what they are up to. Everyone who has graduated from Scholastica is a part of the Scholastica family and we want to keep you close! So, please register with us to receive emails from time to time, and join the Facebook group here.



