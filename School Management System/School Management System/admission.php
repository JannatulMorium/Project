                      <h1>Fees</h1>
                      <p><img src="img/fee.jpg" class="img-responsive" alt="about image">
                    School Fees

Scholastica charges a single annual tuition fee. There is no separately yearly charge. At the time of admission, we charge an Admission Fee as well.

Scholastica offers flexibility for parents in both the payment structure and payment mode for its annual tuition fees. In addition, stationery and textbooks can be purchased through the school for an additional fee, which varies from class to class (this is not mandatory but is designed as a convenience for parents).

For more information about our fees, payment structures and payment modes, please contact our Admissions Office, based in the Mirpur campus at 01817031818.

Admission Fee

The Admission Fee is divided in two parts i.e. actual Admission fee and Security deposit fee.

The Admission Fee is non-refundable. However, after a student leaves the school, the security deposit is refundable if the conditions for a refund are met. Details are provided to parents in the Student-Parent Handbook.

