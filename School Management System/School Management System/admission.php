                      <h1>Financial Assistance</h1>
                      <p><img src="img/FinancialAssistance.jpg" class="img-responsive" alt="about image">
                     Financial Assistance

The Management of Scholastica is committed to provide access to top quality education for the wider community. Through the Scholastica Scholarship Program we have developed support for both underprivileged and meritorious students. The Scholarship Program has two components, as follows:

Merit based Scholarship

Need based Financial Assistance

Merit based Scholarship

As a part of our recognition of hard work, dedication and academic excellence, Scholastica offers Merit Scholarships to one student each from Class V to X. To become eligible for a Merit Scholarship, students must demonstrate excellent academic records. Top students are then invited to sit for the Merit Exam, and the highest achievers are invited for interviews. Through this process, one student is selected to receive the Merit Scholarship from each class.

Need based Financial Assistance

Every year Scholastica provides a large amount of financial assistance to our students. We feel proud that we can assist our students to ensure that their education is not interrupted due to external circumstances outside their control. We look forward to continuing such support for our students for years to come. For further information, parents may email directly to school management.


